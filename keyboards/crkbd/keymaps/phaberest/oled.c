bool oled_task_user(void) {
    // Check if layout indicator should be shown
    if (layout_indicator_active) {
        if (timer_elapsed32(layout_indicator_timer) < 500) {
            // Show layout indicator for 500ms
            oled_clear();
            oled_set_cursor(0, 0);
            if (showing_colemak_indicator) {
                // Try to show COLEMAK image, fallback to text if needed
                oled_write_raw_P(colemak_logo, sizeof(colemak_logo));
                // Also add text overlay for debugging
                oled_set_cursor(0, 7);
                oled_write_P(PSTR("COLEMAK"), false);
            } else {
                // Try to show QWERTY image, fallback to text if needed
                oled_write_raw_P(qwerty_logo, sizeof(qwerty_logo));
                // Also add text overlay for debugging
                oled_set_cursor(0, 7);
                oled_write_P(PSTR("QWERTY"), false);
            }
            return false;
        } else {
            // Timeout reached, disable indicator
            layout_indicator_active = false;
        }
    }

    if (layer_state_is(_MOUSE)) {
        oled_clear();
        oled_set_cursor(0, 0);
        oled_write_raw_P(OMB_logo, sizeof(OMB_logo));
    } else {
        oled_clear();
        oled_render_layer_state();
    }

    return false;
}
